export interface VppZipEntry {
  utility: string;
  city: string;
  county: string;
}

export type VppEligibilityResult =
  | { eligible: true; utility: string; city: string; county: string }
  | { eligible: false };

// Internal helpers ────────────────────────────────────────────────────────────

/** Produce an inclusive array of stringified integers. */
function r(start: number, end: number): string[] {
  return Array.from({ length: end - start + 1 }, (_, i) => String(start + i));
}

/** Map a list of ZIP strings to [zip, VppZipEntry] tuples for Object.fromEntries. */
function e(
  utility: string,
  city: string,
  county: string,
  zips: string[]
): [string, VppZipEntry][] {
  return zips.map((z) => [z, { utility, city, county }] as [string, VppZipEntry]);
}

