/**
 * Look up a ZIP code against the Texas VPP service territory.
 * Strips non-digits and left-pads to 5 characters before lookup.
 * Returns { eligible: true, utility, city, county } or { eligible: false }.
 */
export function checkVppEligibility(zip: string): VppEligibilityResult {
  const key = zip.replace(/\D/g, '').padStart(5, '0');
  const entry = VPP_ZIP_LOOKUP[key];
  return entry
    ? { eligible: true, utility: entry.utility, city: entry.city, county: entry.county }
    : { eligible: false };
}

